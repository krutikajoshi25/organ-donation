const mongoose = require("mongoose");

const notificationSchema = new mongoose.Schema({
  recipientEmail: {
    type: String,
    required: true
  },
  message: {
    type: String,
    required: true
  },
  status: {
    type: String,
    enum: ['Pending', 'Accepted', 'Rejected'],
    default: 'Pending'
  },
  organ: {
    type: [String], // âœ… Allow storing organ(s) like ["Heart"]
    default: []
  },
  isRead: {
    type: Boolean,
    default: false
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model("Notification", notificationSchema);
